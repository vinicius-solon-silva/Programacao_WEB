function somar (a, b) {
    return (a + b)
}

function subtrair (a, b) {
    return (a - b)
}

function multiplicar (a, b) {
    return (a * b)
}

function dividir (a, b) {
    return (a / b)
}

console.log(somar(10, 2));
console.log(subtrair(10, 2));
console.log(multiplicar(10, 2));
console.log(dividir(10, 2));